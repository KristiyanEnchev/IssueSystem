namespace IssueSystem.Api.Infrastructures.Extensions
{
    using System.Threading.Tasks;

    using Microsoft.AspNetCore.Mvc;

    using Microsoft.AspNetCore.Identity;

    public static class ResultExtensions
    {
        public static async Task<ActionResult<TData>> ToActionResult<TData>(this Task<TData> resultTask)
        {
            var result = await resultTask;

            if (result == null)
            {
                return new NotFoundResult();
            }

            return result;
        }

        public static async Task<ActionResult> ToActionResult(this Task<IdentityResult> resultTask)
        {
            var result = await resultTask;

            if (!result.Succeeded)
            {
                return new BadRequestObjectResult(result.Errors);
            }

            return new OkResult();
        }
    }
}
